import crypto from 'node:crypto';

function getKey(): Buffer {
	const b64 = process.env.KEY_ENCRYPTION_KEY_B64;
	if (!b64) throw new Error('Missing KEY_ENCRYPTION_KEY_B64');
	const key = Buffer.from(b64, 'base64');
	if (key.length !== 32) throw new Error('KEY_ENCRYPTION_KEY_B64 must be 32 bytes (base64)');
	return key;
}

export function encryptSecret(plain: string): string {
	const key = getKey();
	const iv = crypto.randomBytes(12);
	const cipher = crypto.createCipheriv('aes-256-gcm', key, iv);
	const enc = Buffer.concat([cipher.update(plain, 'utf8'), cipher.final()]);
	const tag = cipher.getAuthTag();
	return Buffer.concat([iv, tag, enc]).toString('base64');
}

export function decryptSecret(blobB64: string): string {
	const key = getKey();
	const buf = Buffer.from(blobB64, 'base64');
	const iv = buf.subarray(0, 12);
	const tag = buf.subarray(12, 28);
	const data = buf.subarray(28);
	const decipher = crypto.createDecipheriv('aes-256-gcm', key, iv);
	decipher.setAuthTag(tag);
	const dec = Buffer.concat([decipher.update(data), decipher.final()]);
	return dec.toString('utf8');
}



